package com.company;

public class User {
    String firstName;
    String lastName;
    String number;
    String email;
    String adress;
    String password;

    public User(String firstName, String lastName, String number, String email, String adress, String password) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.number = number;
        this.email = email;
        this.adress = adress;
        this.password = password;
    }
}
